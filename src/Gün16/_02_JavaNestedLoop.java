package Gün16;

public class _02_JavaNestedLoop {
    public static void main(String[] args) {
        // aşağıdaki şekilde çıktı veren programı print içerisinde
        // sadece bir tane # kullanarak yazdırınız.
        //          #####
        //          #####
        //          #####
        //          #####
        //          #####

        for (int j=1;j<=5;j++){
            for (int k=1; k<=5; k++){
                System.out.print("#");
            }
            System.out.println();

        }






    }
}
