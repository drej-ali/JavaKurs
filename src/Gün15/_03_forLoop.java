package Gün15;

public class _03_forLoop {
    public static void main(String[] args) {
        // 0 dan 10(dahil) a kadar sayıları ekana yazdırınız.
        // 10(dahil) dan 0 a kadar sayıları ekana yazdırınız.(ayrı bir diger for)

        for (int a = 0; a <= 10; a++)
            System.out.println("a = " + a);


        for (int b = 10; b >= 0; b--)
            System.out.println("b = " + b);



    }
}
